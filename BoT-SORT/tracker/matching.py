def embedding_distance(logger, tracks, detections, metric='euclid', normalize=True ):
    """

    :param tracks: list[STrack]
    :param detections: list[BaseTrack]
    :param metric: 'scipy_cos', 'cosine', 'euclid'
    :param normalize: bool
    :return: cost_matrix np.ndarray

    """

    cost_matrix = np.zeros((len(tracks), len(detections)), dtype=float)
    if cost_matrix.size == 0:
        return cost_matrix

    det_features = np.asarray([track.curr_feat for track in detections], dtype=float)
    track_features = np.asarray([track.smooth_feat for track in tracks], dtype=float)

    if metric=='scipy_cos':
        my_matrix = cdist(track_features, det_features, 'cosine')
    elif metric=='cosine':
        my_matrix = X_cosine_similarity(track_features,det_features, norm=normalize)
    elif metric=='euclid':
        my_matrix = 2 - X_euclidean_dist(track_features, det_features, norm=normalize)
    else:
        raise ValueError(f'The distance {metric} is not supported')

    cost_matrix = np.maximum(0.0, my_matrix)  # / 2.0  # Nomalized features
    return cost_matrix


def X_normalize(x, axis=-1):
    """

        Normalizing to unit length along the specified dimension.
        returns the output into an hypersphere of radious 1.

    Args:
      x: pytorch Variable
    Returns:
      x: pytorch Variable, same shape as input
    """
    x = 1. * x / (torch.norm(x, 2, axis, keepdim=True).expand_as(x) + 1e-12)
    return x


def X_euclidean_dist(x, y, norm=False):
    """
    Args:
      x: pytorch Variable, with shape [m, d]
      y: pytorch Variable, with shape [n, d]
    Returns:
      dist: numpy Variable, with shape [m, n] : float64
    """
    x = torch.tensor(x, dtype=torch.float64) if not isinstance(x, torch.Tensor) else x
    y = torch.tensor(y, dtype=torch.float64) if not isinstance(y, torch.Tensor) else x

    if norm: #shifting the values into a hypersphere of radius 1
        x = X_normalize(x)
        y = X_normalize(y)

    m, n = x.size(0), y.size(0)
    xx = torch.pow(x, 2).sum(1, keepdim=True).expand(m, n)
    yy = torch.pow(y, 2).sum(1, keepdim=True).expand(n, m).t()
    dist = xx + yy
    dist.addmm_(1, -2, x, y.t())
    dist = dist.clamp(min=1e-12).sqrt()  # for numerical stability

    return dist.cpu().numpy()



